package ui;

// Classe abtrata que será herdada pelas classes de cadastro do clinte.
public abstract class Cadastro {
  /**
   * Método abstrato que representa a sessão e login no sistema.
   */
  public abstract void entrarComCadastro();

  /**
   * Metodo abstrato que representa a sessão de cadastro no sistema.
   */
  public abstract void fazerCadastro();

}
