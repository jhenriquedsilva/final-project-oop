package recursos;

/**
* Classe Cores com todas as cores utilizadas no projeto
*/
public class Cores {
    /**
    * @return String com o reset da cor
    */
    public static final String ANSI_RESET = "\u001B[0m";

    /**
    * @return String com a cor vermelha
    */
    public static final String ANSI_RED_2 = "\u001B[1;31m";

    /**
    * @return String com a cor verde
    */
    public static final String ANSI_GREEN_2 = "\u001B[1;32m";

    /**
    * @return String com a cor amarela
    */
    public static final String ANSI_YELLOW_2 = "\u001B[1;33m";

    /**
    * @return String com a cor azul
    */
    public static final String ANSI_BLUE_2 = "\u001B[1;34m";

    /**
    * @return String com a cor magenta
    */
    public static final String ANSI_WHITE_2 = "\u001B[1;37m";
}